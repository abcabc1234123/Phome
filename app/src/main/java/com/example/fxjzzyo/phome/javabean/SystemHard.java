public class SystemHard {
    private int hardId;
    private int phoneId;

    private String os;
    private String cpu;
    private String ghz;
    private String ram;
    private String storage;
    private String storageExpansion;


    public int getHardId() {
        return hardId;
    }

    public void setHardId(int hardId) {
        this.hardId = hardId;
    }

    public int getPhoneId() {
        return phoneId;
    }

    public void setPhoneId(int phoneId) {
        this.phoneId = phoneId;
    }

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }

    public String getCpu() {
        return cpu;
    }

    public void setCpu(String cpu) {
        this.cpu = cpu;
    }

    public String getGhz() {
        return ghz;
    }

    public void setGhz(String ghz) {
        this.ghz = ghz;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getStorage() {
        return storage;
    }

    public void setStorage(String storage) {
        this.storage = storage;
    }

    public String getStorageExpansion() {
        return storageExpansion;
    }

    public void setStorageExpansion(String storageExpansion) {
        this.storageExpansion = storageExpansion;
    }
}
