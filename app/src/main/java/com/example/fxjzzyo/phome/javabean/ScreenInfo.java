public class ScreenInfo {
    private int screenId;
    private int phoneId;


    private String screenSize;
    private String screenColorNum;
    private String screenResolution;
    private String pixelDesity;
    private String touchWay;

    public int getScreenId() {
        return screenId;
    }

    public void setScreenId(int screenId) {
        this.screenId = screenId;
    }

    public int getPhoneId() {
        return phoneId;
    }

    public void setPhoneId(int phoneId) {
        this.phoneId = phoneId;
    }

    public String getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }

    public String getScreenColorNum() {
        return screenColorNum;
    }

    public void setScreenColorNum(String screenColorNum) {
        this.screenColorNum = screenColorNum;
    }

    public String getScreenResolution() {
        return screenResolution;
    }

    public void setScreenResolution(String screenResolution) {
        this.screenResolution = screenResolution;
    }

    public String getPixelDesity() {
        return pixelDesity;
    }

    public void setPixelDesity(String pixelDesity) {
        this.pixelDesity = pixelDesity;
    }

    public String getTouchWay() {
        return touchWay;
    }

    public void setTouchWay(String touchWay) {
        this.touchWay = touchWay;
    }
}
